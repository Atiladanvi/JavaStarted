class Wrapper {
    /**
     * Print value
     * @param s Integer
     */
    static void integer(Integer s){
       System.out.println(s);
    }
    /**
     * Print value
     * @param s String
     */
    static void string(String s){
        System.out.println(s);
    }
    /**
     * Print value
     * @param s Character
     */
    static void character(Character s){
        System.out.println(s);
    }
    /**
     * Print value
     * @param s Boolean
     */
    static void bool(Boolean s){
        System.out.println(s);
    }
}
